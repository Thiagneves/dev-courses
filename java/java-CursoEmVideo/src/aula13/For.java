package aula13;

public class For {
    public static void main(String[] args) {

        for (int cc = 0; cc <= 3; cc++){
            System.out.println(cc);
        }

        System.out.println(" ");

        for (int j = 0; j <= 10; ){
            System.out.println(j);
            j+=2;
        }

        System.out.println(" ");

        for (int k = 1; k <= 100; k += 10){
            System.out.println(k);
        }
    }
}
